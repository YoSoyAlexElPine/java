package funciones;

/**
 * Clase que define constantes para representar colores de texto en la consola utilizando códigos ANSI.
 * Cada constante representa un color específico.
 */
public class Colores {
    public static final String ANSI_RESET = "\u001B[0m";     // Restablece el color del texto a su valor por defecto.
    public static final String ANSI_BLACK = "\u001B[30m";    // Color negro.
    public static final String ANSI_RED = "\u001B[31m";      // Color rojo.
    public static final String ANSI_GREEN = "\u001B[32m";    // Color verde.
    public static final String ANSI_YELLOW = "\u001B[33m";   // Color amarillo.
    public static final String ANSI_BLUE = "\u001B[34m";     // Color azul.
    public static final String ANSI_PURPLE = "\u001B[35m";   // Color morado.
    public static final String ANSI_CYAN = "\u001B[36m";     // Color cyan.
    public static final String ANSI_WHITE = "\u001B[37m";    // Color blanco.
}
